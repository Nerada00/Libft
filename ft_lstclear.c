#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*current;
	t_list	*tmp;

	if (!lst || !del)
		return ;
	current = *lst;
	while (current != NULL)
	{
		(*del)(current->content);
		tmp = current->next;
		free(current);
		current = tmp;
	}
	*lst = NULL;
}
